export interface EmitOptions {
  /** When true (the user passed `--release`), skip `#line` directives — the
   *  resulting C compiles a touch faster and the binary is meant to be
   *  shipped, not stepped through. Default false : debug builds get `#line`
   *  threaded through every op so `gdb`/`lldb` show Vader source. */
  readonly release?: boolean;
}

export function emitC(m: BytecodeModule, opts: EmitOptions = {}): string {
  const ctx = newCtx(m, opts.release === true);
  const out: string[] = [];

  out.push(`/* Generated by vader — do not edit. */`);
  out.push(`#include "vader.h"`);
  out.push(`#include <stddef.h>`);
  out.push(`#include <stdlib.h>`);     /* atexit (registered in main) */
  out.push(`#include <string.h>`);
  out.push(``);

  emitTypeDecls(ctx, out);
  emitForwardDecls(ctx, out);
  emitFnSigTypedefs(ctx, out);
  emitFnTrampolines(ctx, out);
  emitTypeInfoTable(ctx, out);
  emitImportShims(ctx, out);
  emitVtableForwardDecls(ctx, out);
  emitFunctions(ctx, out);
  emitVtableDispatchers(ctx, out);
  emitMain(ctx, out);

  return out.join("\n") + "\n";
}

