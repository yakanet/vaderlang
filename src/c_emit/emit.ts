// BytecodeModule → C source (§1.9). Reads the in-memory bytecode emitted by
// the bytecode pass and produces a single .c file ready to compile against
// runtime/c/vader_runtime.c.
//
// Strategy:
// - Each fn is emitted as a C function with a generated C signature.
// - The bytecode's value stack maps to a chain of generated C tmp vars (one
//   per push); the C compiler folds the chain back into expression form.
// - Structured control (`block`/`loop`/`if`) uses C labels + goto for `br`
//   semantics. The label naming derives from the open-ip so jumps resolve
//   without a runtime label stack.
// - Primitives flow as their native C type; struct/array refs as `void*`;
//   union slots and `any` as `vader_box_t`. The emitter inserts box/unbox
//   sequences whenever a value flows across the boundary (γ representation).
// - Imports for `std/io` map to the runtime's `vader_*` functions; the
//   tag-aware ones (read_file, read_line, write_file) receive the BcType
//   indices for the `string` and `Error` variants resolved per module.

import type {BytecodeModule, BcFunction, BcImport, BcSignature} from "../bytecode/module.ts";
import type { Op } from "../bytecode/ops.ts";
import type { BcType, ValType } from "../bytecode/types.ts";

import {
  cTypeFor, cTypeForVal, cTypeForValBare, emitFunctions, isRefVal,
  signatureFor, valTypeOfBcType, zeroInit,
} from "./body.ts";
import { emitVtableDispatchers, emitVtableForwardDecls } from "./ops.ts";

export function emitC(m: BytecodeModule): string {
  const ctx = newCtx(m);
  const out: string[] = [];

  out.push(`/* Generated by vader — do not edit. */`);
  out.push(`#include "vader.h"`);
  out.push(`#include <stddef.h>`);
  out.push(`#include <stdlib.h>`);     /* atexit (registered in main) */
  out.push(`#include <string.h>`);
  out.push(``);

  emitTypeDecls(ctx, out);
  emitForwardDecls(ctx, out);
  emitFnSigTypedefs(ctx, out);
  emitFnTrampolines(ctx, out);
  emitTypeInfoTable(ctx, out);
  emitImportShims(ctx, out);
  emitVtableForwardDecls(ctx, out);
  emitFunctions(ctx, out);
  emitVtableDispatchers(ctx, out);
  emitMain(ctx, out);

  return out.join("\n") + "\n";
}

// =========================================================================
// Context
// =========================================================================

export interface EmitCtx {
  readonly module: BytecodeModule;
  readonly stringTagIndex: number;       // -1 if absent
  readonly errorTagIndex: number;        // -1 if absent
  /** C identifier for each fn slot (mangled name, sanitised). */
  readonly fnNames: readonly string[];
  /** C identifier for each struct (BcType slot). */
  readonly structNames: readonly (string | null)[];
  /** Tag value for each primitive ValType — used when boxing a typed value
   *  into a `ref`/`any` slot (e.g. passing `i32` to a fn expecting `ref`).
   *  -1 means the type isn't materialised in this module's type table.  */
  readonly primitiveTagOf: ReadonlyMap<ValType, number>;
  /** Inverse of `module.implTable`: trait name → struct type-indices that
   *  implement it. Built once so `is Trait` checks don't re-scan the table. */
  readonly structIdxsByTrait: ReadonlyMap<string, readonly number[]>;
  /** Fn indices that may allocate — directly via a `struct.new` / `array.new`
   *  / `string.concat` / `intrinsic` / `make_closure`, indirectly via a
   *  `call.import` / `call.indirect` / `virtual.call` (conservative), or
   *  transitively via a `call` to another may-alloc fn. Fns NOT in this set
   *  skip the shadow-stack frame entirely — no `vader_gc_frame_t` setup,
   *  no `gc_top` push/pop, no `gc_roots[]` array. */
  readonly mayAlloc: ReadonlySet<number>;
}

function newCtx(m: BytecodeModule): EmitCtx {
  const fnNames = m.functions.map((f) => sanitise(f.name));
  const structNames: (string | null)[] = m.types.map((t) =>
    t.kind === "struct" ? `vader_struct_${sanitise(t.name)}_t` : null);
  let stringIdx = -1, errorIdx = -1;
  const primitiveTagOf = new Map<ValType, number>();
  for (let i = 0; i < m.types.length; i++) {
    const t = m.types[i]!;
    if (t.kind === "primitive") {
      primitiveTagOf.set(t.val, i);
      if (t.val === "string") stringIdx = i;
    }
    if (t.kind === "ref" && t.traitName === "Error") errorIdx = i;
  }
  const structIdxsByTrait = new Map<string, number[]>();
  for (const [structIdx, traits] of m.implTable) {
    for (const traitName of traits) {
      const list = structIdxsByTrait.get(traitName);
      if (list !== undefined) list.push(structIdx);
      else structIdxsByTrait.set(traitName, [structIdx]);
    }
  }
  return {
    module: m, stringTagIndex: stringIdx, errorTagIndex: errorIdx,
    fnNames, structNames, primitiveTagOf, structIdxsByTrait,
    mayAlloc: computeMayAlloc(m),
  };
}

// Bytecode-level callgraph analysis: which fns may trigger a GC allocation
// (transitively). Used by `emitFunctionBody` to skip the shadow-stack frame
// for pure leaf-ish fns — `inc(x) = x + 1`, accessor methods, predicates.
//
// Direct allocators: `struct.new`, `array.new`, `array.push`, `string.concat`,
// `intrinsic` (string-builder family), `make_closure`. Indirect allocators
// (conservative — unknown target): `call.import`, `call.indirect`,
// `virtual.call`. Transitive: any `call` to a fn already in the set.
function computeMayAlloc(m: BytecodeModule): Set<number> {
  const mayAlloc = new Set<number>();

  for (let fi = 0; fi < m.functions.length; fi++) {
    const fn = m.functions[fi]!;
    for (const op of fn.body) {
      switch (op.kind) {
        case "struct.new":
        case "array.new":
        case "array.push":
        case "string.concat":
        case "intrinsic":
        case "make_closure":
        case "call.import":
        case "call.indirect":
        case "virtual.call":
          mayAlloc.add(fi);
          break;
      }
      if (mayAlloc.has(fi)) break;
    }
  }

  let changed = true;
  while (changed) {
    changed = false;
    for (let fi = 0; fi < m.functions.length; fi++) {
      if (mayAlloc.has(fi)) continue;
      const fn = m.functions[fi]!;
      for (const op of fn.body) {
        if (op.kind === "call" && mayAlloc.has(op.fnIndex)) {
          mayAlloc.add(fi);
          changed = true;
          break;
        }
      }
    }
  }

  return mayAlloc;
}

// =========================================================================
// Type declarations
// =========================================================================

function emitTypeDecls(ctx: EmitCtx, out: string[]): void {
  // Forward declarations for every struct so field references compile in any
  // order; bodies follow.
  for (let i = 0; i < ctx.module.types.length; i++) {
    const t = ctx.module.types[i]!;
    if (t.kind === "struct") {
      out.push(`typedef struct ${ctx.structNames[i]!} ${ctx.structNames[i]!};`);
    }
  }
  out.push(``);

  for (let i = 0; i < ctx.module.types.length; i++) {
    const t = ctx.module.types[i]!;
    if (t.kind !== "struct") continue;
    const cname = ctx.structNames[i]!;
    out.push(`struct ${cname} {`);
    out.push(`    vader_obj_header_t header;`);
    for (const field of t.fields) {
      const fty = cTypeFor(ctx, ctx.module.types[field.typeIndex]!);
      out.push(`    ${fty} f_${sanitise(field.name)};`);
    }
    out.push(`};`);
  }
  out.push(``);

  // String literals as named struct constants — one decl per pool entry,
  // pre-sized to the UTF-8 byte length. The C compiler interns the data
  // array regardless ; folding the pointer + length into one
  // `vader_string_t` static lets `string.const` resolve to a bare name
  // instead of a `vader_string_new(_data, _len)` call.
  for (let i = 0; i < ctx.module.strings.length; i++) {
    const s = ctx.module.strings[i]!;
    const bytes = new TextEncoder().encode(s);
    out.push(`static const vader_string_t vader_str_${i} = { ${cStringLitFromBytes(bytes)}, ${bytes.length} };`);
  }
  out.push(``);
}

// =========================================================================
// Forward declarations
// =========================================================================

function emitForwardDecls(ctx: EmitCtx, out: string[]): void {
  for (let i = 0; i < ctx.module.functions.length; i++) {
    const fn = ctx.module.functions[i]!;
    out.push(`static ${signatureFor(ctx, fn)};`);
  }
  out.push(``);
}

// =========================================================================
// Function-pointer typedefs and per-fn trampolines (for fn values / closures)
// =========================================================================

function emitFnSigTypedefs(ctx: EmitCtx, out: string[]): void {
  // One typedef per BcFn type. Every callable signature includes a leading
  // `void* env` so closures (Phase 3) can be invoked through the same path.
  // Globals route through a per-fn trampoline that ignores env.
  for (let i = 0; i < ctx.module.types.length; i++) {
    const t = ctx.module.types[i]!;
    if (t.kind !== "fn") continue;
    const paramVals = t.params.map((p) => valTypeOfBcType(ctx.module.types[p]!));
    const retVal = valTypeOfBcType(ctx.module.types[t.returnType]!);
    const params = paramVals.map((p, j) => `${cTypeForValBare(p)} a${j}`);
    const cret = retVal === "void" ? "void" : cTypeForValBare(retVal);
    const head = `void* env`;
    out.push(`typedef ${cret} (*vader_fn_sig_${i}_t)(${params.length === 0 ? head : `${head}, ${params.join(", ")}`});`);
  }
  out.push(``);
}

// =========================================================================
// Type info table — pointer maps consumed by the GC scanner
// =========================================================================

function emitTypeInfoTable(ctx: EmitCtx, out: string[]): void {
  const types = ctx.module.types;

  // Per-type ptr-offset arrays. We collect into a buffer first so the table
  // initialiser can reference each by name. `offsetof` is resolved at C compile
  // time so we don't have to know struct sizes in TypeScript.
  //
  // Two parallel arrays per struct:
  //   - `_ptrs[]`     box-bearing fields (vader_box_t)
  //   - `_strs[]`     raw vader_string_t fat-ptr fields
  // The string GC needs the latter because string fields are stored inline
  // as primitive `vader_string_t`, not as boxes — the box-aliased mark
  // walk would otherwise miss them.
  for (let i = 0; i < types.length; i++) {
    const t = types[i]!;
    if (t.kind === "struct") {
      const cname = ctx.structNames[i]!;
      const offsets: string[] = [];
      const stringOffsets: string[] = [];
      for (const field of t.fields) {
        const ftype = types[field.typeIndex]!;
        if (ftype.kind !== "primitive") {
          // Heap-bearing field — its C type is `vader_box_t`. Tracing this
          // slot requires checking the box tag at scan time.
          offsets.push(`offsetof(${cname}, f_${sanitise(field.name)})`);
        } else if (ftype.val === "string") {
          // Raw `vader_string_t` field. The Cheney scan ignores it (the
          // buffer lives in the non-moving string arena), but the string
          // mark-sweep collector must reach it.
          stringOffsets.push(`offsetof(${cname}, f_${sanitise(field.name)})`);
        }
      }
      if (offsets.length > 0) {
        out.push(`static const uint16_t vader_type_${i}_ptrs[] = { ${offsets.join(", ")} };`);
      }
      if (stringOffsets.length > 0) {
        out.push(`static const uint16_t vader_type_${i}_strs[] = { ${stringOffsets.join(", ")} };`);
      }
    } else if (t.kind === "fn") {
      // vader_fn_t has `code` (function pointer — NOT a heap ref, must not
      // be scanned) and `env` (heap pointer to the closure environment).
      out.push(`static const uint16_t vader_type_${i}_ptrs[] = { offsetof(vader_fn_t, env) };`);
    } else if (t.kind === "array") {
      // The buf is a raw pointer to a header-bearing object — scan_raw treatment.
      out.push(`static const uint16_t vader_type_${i}_ptrs[] = { offsetof(vader_array_t, buf) };`);
    }
  }

  out.push(``);
  // Fixed length so missing entries (primitives, unions, refs — no GC info)
  // zero-initialise to `VADER_TYPE_KIND_NONE`. Without the explicit size, C
  // would size the array to `max-designated-index + 1`, and any later
  // `vader_type_info_table[tag]` read with `tag` past the last designated
  // entry but below `vader_type_info_count` would walk off the array.
  out.push(`const vader_type_info_t vader_type_info_table[${types.length}] = {`);
  for (let i = 0; i < types.length; i++) {
    const t = types[i]!;
    if (t.kind === "struct") {
      const cname = ctx.structNames[i]!;
      const hasPtrs = t.fields.some((f) => types[f.typeIndex]!.kind !== "primitive");
      const ptrArr = hasPtrs ? `vader_type_${i}_ptrs` : "NULL";
      const ptrCount = hasPtrs ? t.fields.filter((f) => types[f.typeIndex]!.kind !== "primitive").length : 0;
      const strFields = t.fields.filter((f) => {
        const ft = types[f.typeIndex]!;
        return ft.kind === "primitive" && ft.val === "string";
      });
      const hasStrs = strFields.length > 0;
      const strArr = hasStrs ? `vader_type_${i}_strs` : "NULL";
      const strCount = strFields.length;
      out.push(`    [${i}] = { VADER_TYPE_KIND_STRUCT, sizeof(${cname}), ${ptrArr}, ${ptrCount}, ${strCount}, ${strArr} },`);
    } else if (t.kind === "fn") {
      out.push(`    [${i}] = { VADER_TYPE_KIND_FN, sizeof(vader_fn_t), vader_type_${i}_ptrs, 1, 0, NULL },`);
    } else if (t.kind === "array") {
      out.push(`    [${i}] = { VADER_TYPE_KIND_ARRAY, sizeof(vader_array_t), vader_type_${i}_ptrs, 1, 0, NULL },`);
    }
    // primitive / union / ref → entry implicitly zero-initialised
    // (VADER_TYPE_KIND_NONE), which the GC interprets as "non-heap, skip".
  }
  out.push(`};`);
  out.push(`const size_t vader_type_info_count = ${types.length};`);
  out.push(``);
}

function emitFnTrampolines(ctx: EmitCtx, out: string[]): void {
  // Walk every fn body to discover which fns are taken by reference and
  // which lifted fns get instantiated via `make_closure`. Each kind needs a
  // matching wrapper:
  //   - `fn.ref`        — wrapper drops env (global fn used as a value)
  //   - `make_closure`  — wrapper boxes env into vader_box_t before calling
  //                       (the lifted fn's signature takes `ref env`)
  const fnRefMap = new Map<number, number>();        // fnIndex → BcFn typeIndex
  const closureMap = new Map<number, number>();      // lifted fnIndex → BcFn typeIndex
  for (const fn of ctx.module.functions) {
    for (const op of fn.body) {
      if (op.kind === "fn.ref") fnRefMap.set(op.fnIndex, op.typeIndex);
      else if (op.kind === "make_closure") closureMap.set(op.fnIndex, op.typeIndex);
    }
  }
  if (fnRefMap.size === 0 && closureMap.size === 0) return;

  for (const [fnIndex, typeIndex] of fnRefMap) {
    const fn = ctx.module.functions[fnIndex]!;
    const cname = ctx.fnNames[fnIndex]!;
    const params = fn.signature.params.map((p, j) => `${cTypeForValBare(p)} a${j}`);
    const cret = fn.signature.result === "void" ? "void" : cTypeForValBare(fn.signature.result);
    const argList = fn.signature.params.map((_, j) => `a${j}`).join(", ");
    out.push(`static ${cret} vader_fn_tramp_${fnIndex}(${params.length === 0 ? "void* env" : `void* env, ${params.join(", ")}`}) {`);
    out.push(`    (void) env;`);
    if (fn.signature.result === "void") {
      out.push(`    ${cname}(${argList});`);
    } else {
      out.push(`    return ${cname}(${argList});`);
    }
    out.push(`}`);
    out.push(`static vader_fn_t vader_fn_static_${fnIndex} = { { ${typeIndex}u, 0u, 0u, 0u, NULL }, (void*) &vader_fn_tramp_${fnIndex}, NULL };`);
  }

  // Lifted-lambda wrappers — the bytecode signature of a lifted fn is
  // `(ref, ...originalParams) → ret` where the leading `ref` is the env
  // boxed as vader_box_t. The wrapper takes `void* env`, boxes it, and
  // invokes the underlying lifted fn. The `make_closure` op grabs the
  // wrapper's address at allocation time.
  for (const [fnIndex, _typeIndex] of closureMap) {
    const fn = ctx.module.functions[fnIndex]!;
    const cname = ctx.fnNames[fnIndex]!;
    if (fn.signature.params.length === 0) {
      // Defensive — a lifted fn always has at least the env param.
      continue;
    }
    // Skip the env param at index 0; the rest mirror the original lambda's params.
    const tailParams = fn.signature.params.slice(1).map((p, j) => `${cTypeForValBare(p)} a${j}`);
    const tailArgs = fn.signature.params.slice(1).map((_, j) => `a${j}`).join(", ");
    const cret = fn.signature.result === "void" ? "void" : cTypeForValBare(fn.signature.result);
    const head = tailParams.length === 0 ? "void* env" : `void* env, ${tailParams.join(", ")}`;
    out.push(`static ${cret} vader_fn_lift_${fnIndex}(${head}) {`);
    // Box raw env back into the vader_box_t shape the lifted fn expects.
    // The BcFn type index of the env doesn't matter to the lifted fn body —
    // only the raw obj pointer is dereferenced via struct.get on the env
    // struct's fields. Tag 0 is reserved for null and is harmless here.
    out.push(`    vader_box_t env_box; env_box.tag = 0u; env_box._pad = 0u; env_box.payload.obj = env;`);
    const callArgs = tailArgs.length === 0 ? `env_box` : `env_box, ${tailArgs}`;
    if (fn.signature.result === "void") {
      out.push(`    ${cname}(${callArgs});`);
    } else {
      out.push(`    return ${cname}(${callArgs});`);
    }
    out.push(`}`);
  }
  out.push(``);
}

// =========================================================================
// Import shims
// =========================================================================

function emitImportShims(ctx: EmitCtx, out: string[]): void {
  // Unused-import pruning happens upstream in `pruneUnusedImports` (see
  // `src/midir/emit.ts`) so the bytecode module already carries only the
  // imports actually referenced by `call.import` ops. Every backend (VM,
  // C-emit, future WASM) and every serialiser (`.vir` text/binary) gets
  // the compact table for free.
  for (let i = 0; i < ctx.module.imports.length; i++) {
    const imp = ctx.module.imports[i]!;
    const shim = importShim(ctx, imp, i);
    if (shim !== null) out.push(shim);
  }
  out.push(``);
}

function importShim(ctx: EmitCtx, imp: BcImport, idx: number): string | null {
  const name = `vader_import_${idx}`;
  const params = imp.signature.params.map((vt, j) => `${cTypeForVal(ctx, vt)} a${j}`).join(", ");
  const ret = cTypeForVal(ctx, imp.signature.result);
  const head = `static ${ret} ${name}(${params || "void"})`;

  // Built-in std/io routes — implemented by the runtime.
  switch (imp.mangledName) {
    case "std_io$print":
      return `${head} { vader_print(a0); }`;
    case "std_io$println":
      return `${head} { vader_println(a0); }`;
    case "std_testing$panic":
      return `${head} { vader_panic(a0); }`;
    case "std_io$eprint":
      return `${head} { vader_eprint(a0); }`;
    case "std_io$eprintln":
      return `${head} { vader_eprintln(a0); }`;
    case "std_io$exists":
      return `${head} { return vader_exists(a0); }`;
    case "std_io$is_dir":
      return `${head} { return vader_is_dir(a0); }`;
    case "std_io$read_dir": {
      const strIdx = ctx.stringTagIndex;
      if (strIdx < 0) return `${head} { vader_trap("read_dir: no string type"); }`;
      const arrIdx = ctx.module.types.findIndex(t => t.kind === "array" && t.element === strIdx);
      if (arrIdx < 0) return `${head} { vader_trap("read_dir: no [string] type"); }`;
      return `${head} { return vader_read_dir(a0, ${arrIdx}u, ${strIdx}u, ${tagOrTrap(ctx, "error")}); }`;
    }
    case "std_io$read_file":
      return `${head} { return vader_read_file(a0, ${tagOrTrap(ctx, "string")}, ${tagOrTrap(ctx, "error")}); }`;
    case "std_io$write_file":
      return `${head} { return vader_write_file(a0, a1, 0u, ${tagOrTrap(ctx, "error")}); }`;
    case "std_io$read_line":
      return `${head} { return vader_read_line(${tagOrTrap(ctx, "string")}, ${tagOrTrap(ctx, "error")}); }`;
    case "std_io$read_stdin":
      return `${head} { return vader_read_stdin(a0, ${tagOrTrap(ctx, "string")}, ${tagOrTrap(ctx, "error")}); }`;

    case "std_string$byte_len":    return `${head} { return vader_string_byte_len(a0); }`;
    case "std_string$slice":       return `${head} { return vader_string_slice(a0, a1, a2); }`;
    case "std_string$contains":    return `${head} { return vader_string_contains(a0, a1); }`;
    case "std_string$starts_with": return `${head} { return vader_string_starts_with(a0, a1); }`;
    case "std_string$ends_with":   return `${head} { return vader_string_ends_with(a0, a1); }`;
    case "std_string$trim":        return `${head} { return vader_string_trim(a0); }`;
    case "std_string$to_upper":    return `${head} { return vader_string_to_upper(a0); }`;
    case "std_string$to_lower":    return `${head} { return vader_string_to_lower(a0); }`;
    // `string implements Index(usize, char)` is `@intrinsic`-impl in std/core,
    // so the host provides the body under the impl-method mangled name.
    case "std_core$string$Index$at":
      return `${head} { return vader_string_char_at(a0, a1); }`;
    case "std_string$byte_at":
      return `${head} { return vader_string_byte_at(a0, a1); }`;
    case "std_string$split": {
      const strIdx = ctx.stringTagIndex;
      if (strIdx < 0) return `${head} { vader_trap("split: no string type"); }`;
      const arrIdx = ctx.module.types.findIndex(t => t.kind === "array" && t.element === strIdx);
      if (arrIdx < 0) return `${head} { vader_trap("split: no [string] type"); }`;
      return `${head} { return vader_box_obj(${arrIdx}u, vader_string_split(a0, a1, ${arrIdx}u, ${strIdx}u)); }`;
    }
    case "std_string$parse_int":
      return `${head} { return vader_string_parse_int(a0, ${primTagOrTrap(ctx, "i32")}, ${tagOrTrap(ctx, "error")}); }`;
    case "std_string$parse_float":
      return `${head} { return vader_string_parse_float(a0, ${primTagOrTrap(ctx, "f64")}, ${tagOrTrap(ctx, "error")}); }`;
    case "std_core$string$Hash$hash":
      return `${head} { return vader_string_hash(a0); }`;

    // `@intrinsic <T> implements Display` for primitives — each shim spins
    // up a one-shot StringBuilder, calls the matching append_display_<T>
    // helper, then finishes. Reuses existing runtime infrastructure rather
    // than introducing parallel `vader_<T>_to_string` helpers.
    case "std_core$i8$Display$to_string":
    case "std_core$i16$Display$to_string":
    case "std_core$i32$Display$to_string":
      return `${head} { vader_builder_t* sb = vader_builder_new(); vader_builder_append_display_i32(sb, (int32_t) a0); return vader_builder_finish(sb); }`;
    case "std_core$i64$Display$to_string":
    case "std_core$isize$Display$to_string":
      return `${head} { vader_builder_t* sb = vader_builder_new(); vader_builder_append_display_i64(sb, (int64_t) a0); return vader_builder_finish(sb); }`;
    case "std_core$u8$Display$to_string":
    case "std_core$u16$Display$to_string":
    case "std_core$u32$Display$to_string":
      return `${head} { vader_builder_t* sb = vader_builder_new(); vader_builder_append_display_u32(sb, (uint32_t) a0); return vader_builder_finish(sb); }`;
    case "std_core$u64$Display$to_string":
    case "std_core$usize$Display$to_string":
      return `${head} { vader_builder_t* sb = vader_builder_new(); vader_builder_append_display_u64(sb, (uint64_t) a0); return vader_builder_finish(sb); }`;
    case "std_core$f32$Display$to_string":
      return `${head} { vader_builder_t* sb = vader_builder_new(); vader_builder_append_display_f32(sb, a0); return vader_builder_finish(sb); }`;
    case "std_core$f64$Display$to_string":
      return `${head} { vader_builder_t* sb = vader_builder_new(); vader_builder_append_display_f64(sb, a0); return vader_builder_finish(sb); }`;
    case "std_core$bool$Display$to_string":
      return `${head} { vader_builder_t* sb = vader_builder_new(); vader_builder_append_display_bool(sb, a0); return vader_builder_finish(sb); }`;
    case "std_core$char$Display$to_string":
      return `${head} { vader_builder_t* sb = vader_builder_new(); vader_builder_append_display_char(sb, a0); return vader_builder_finish(sb); }`;
    case "std_core$string$Display$to_string":
      return `${head} { return a0; }`;

    case "std_string_builder$StringBuilder$Display$to_string": {
      // `@intrinsic StringBuilder implements Display` — receiver is a boxed
      // StringBuilder whose only field `parts` holds the string[] buffer.
      // The mangled struct name is `<module>$StringBuilder` (or just
      // `StringBuilder` when the module stem matches the type name).
      const sbIdx = ctx.module.types.findIndex(
        (t) => t.kind === "struct"
            && (t.name === "StringBuilder" || t.name.endsWith("$StringBuilder")),
      );
      if (sbIdx < 0) return `${head} { vader_trap("StringBuilder type not found"); }`;
      const sbCName = ctx.structNames[sbIdx]!;
      return `${head} { vader_box_t parts = ((${sbCName}*) a0.payload.obj)->f_parts; return vader_string_concat_all((vader_array_t*) parts.payload.obj); }`;
    }

    case "std_runtime$collect":      return `${head} { vader_gc_collect(); }`;
    case "std_runtime$collections":  return `${head} { return (int32_t) vader_gc_get_stats().total_collections; }`;
    case "std_runtime$bytes_used":   return `${head} { return (int32_t) vader_gc_get_stats().bytes_used; }`;
    case "std_runtime$bytes_copied": return `${head} { return (int32_t) vader_gc_get_stats().total_copied; }`;

    case "std_math$sqrt":  return `${head} { return vader_math_sqrt(a0); }`;
    case "std_math$pow":   return `${head} { return vader_math_pow(a0, a1); }`;
    case "std_math$floor": return `${head} { return vader_math_floor(a0); }`;
    case "std_math$ceil":  return `${head} { return vader_math_ceil(a0); }`;
    case "std_math$round": return `${head} { return vader_math_round(a0); }`;
    case "std_math$sin":   return `${head} { return vader_math_sin(a0); }`;
    case "std_math$cos":   return `${head} { return vader_math_cos(a0); }`;
    case "std_math$tan":   return `${head} { return vader_math_tan(a0); }`;

    case "std_process$spawn_run":
      return `${head} { return vader_spawn_run((vader_array_t*) a0.payload.obj); }`;
    case "std_process$spawn_last_stdout":
      return `${head} { return vader_spawn_last_stdout(); }`;
    case "std_process$spawn_last_stderr":
      return `${head} { return vader_spawn_last_stderr(); }`;

    default:
      // Foreign imports — emit a stub that traps, the user supplies linkage
      // via `cc <user>.o` post-MVP.
      return `${head} { vader_trap("unbound import: ${imp.mangledName}"); }`;
  }
}

function tagOrTrap(ctx: EmitCtx, kind: "string" | "error"): string {
  const idx = kind === "string" ? ctx.stringTagIndex : ctx.errorTagIndex;
  if (idx < 0) return `0u /* ${kind} tag absent */`;
  return `${idx}u`;
}

/** Primitive-type tag lookup for runtime calls that must box their result with
 *  the user-visible type's BcType index (e.g. `parse_int` boxing as `i32` so
 *  `match r { is i32 -> ... }` succeeds). */
function primTagOrTrap(ctx: EmitCtx, val: ValType): string {
  const idx = ctx.primitiveTagOf.get(val);
  if (idx === undefined) return `0u /* ${val} tag absent */`;
  return `${idx}u`;
}

// =========================================================================
// `main` entry
// =========================================================================

function emitMain(ctx: EmitCtx, out: string[]): void {
  const main = ctx.module.functions.findIndex((f) => f.isMain && f.body.length > 0);
  if (main < 0) {
    out.push(`int main(int argc, char** argv) { (void)argc; (void)argv; return 0; }`);
    return;
  }
  const fn = ctx.module.functions[main]!;
  const cname = ctx.fnNames[main]!;
  // Typechecker (T3033) guarantees the signature is `()` or `([string])` → i32.
  const takesArgv = fn.signature.params.length === 1;
  out.push(`int main(int argc, char** argv) {`);
  // Register the GC arena release at exit. Process death frees on Unix, but
  // an explicit shutdown is correct for embedded / atexit-aware tooling and
  // gives sanitisers a clean baseline.
  out.push(`    atexit(vader_gc_shutdown);`);
  let callArgs = "";
  if (takesArgv) {
    const strIdx = ctx.stringTagIndex;
    const arrIdx = ctx.module.types.findIndex(t => t.kind === "array" && t.element === strIdx);
    if (strIdx < 0 || arrIdx < 0) {
      out.push(`    (void)argc; (void)argv; vader_trap("main(argv): missing [string] type"); return 1;`);
      out.push(`}`);
      return;
    }
    out.push(`    vader_box_t __args = vader_box_obj(${arrIdx}u, vader_runtime_argv(argc, argv, ${arrIdx}u, ${strIdx}u));`);
    callArgs = "__args";
  } else {
    out.push(`    (void)argc; (void)argv;`);
  }
  if (fn.signature.result === "void") {
    out.push(`    ${cname}(${callArgs});`);
    out.push(`    return 0;`);
  } else {
    out.push(`    return (int) ${cname}(${callArgs});`);
  }
  out.push(`}`);
}

