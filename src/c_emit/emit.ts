export interface EmitCtx {
  readonly module: BytecodeModule;
  readonly stringTagIndex: number;       // -1 if absent
  readonly errorTagIndex: number;        // -1 if absent
  /** C identifier for each fn slot (mangled name, sanitised). */
  readonly fnNames: readonly string[];
  /** C identifier for each struct (BcType slot). */
  readonly structNames: readonly (string | null)[];
  /** Tag value for each primitive ValType — used when boxing a typed value
   *  into a `ref`/`any` slot (e.g. passing `i32` to a fn expecting `ref`).
   *  -1 means the type isn't materialised in this module's type table.  */
  readonly primitiveTagOf: ReadonlyMap<ValType, number>;
  /** Inverse of `module.implTable`: trait name → struct type-indices that
   *  implement it. Built once so `is Trait` checks don't re-scan the table. */
  readonly structIdxsByTrait: ReadonlyMap<string, readonly number[]>;
  /** Fn indices that may allocate — directly via a `struct.new` / `array.new`
   *  / `string.concat` / `intrinsic` / `make_closure`, indirectly via a
   *  `call.import` / `call.indirect` / `virtual.call` (conservative), or
   *  transitively via a `call` to another may-alloc fn. Fns NOT in this set
   *  skip the shadow-stack frame entirely — no `vader_gc_frame_t` setup,
   *  no `gc_top` push/pop, no `gc_roots[]` array. */
  readonly mayAlloc: ReadonlySet<number>;
  /** Struct type indices that appear as a target of `struct.set` somewhere
   *  in the module. These structs need a heap-allocated body so the mutation
   *  is observable to every alias of the value ; the inline-variant rep
   *  (encoded directly in `vader_box_t.payload`) only fits truly immutable
   *  values. Closure environments, `Counter { value: i32 }`-style mutables,
   *  and any single-field struct the user writes to fall into this set. */
  readonly mutatedStructs: ReadonlySet<number>;
  /** Concrete struct type-index → its all-`Any` counterpart type-index, when
   *  one was registered (via erasure dedupe's extra entry). Used at the
   *  virtual.call / call boundary : if the runtime receiver carries the
   *  Any-counterpart tag, the C-emit re-packs the heap layout into the
   *  expected inline-eligible struct's payload shape. */
  readonly anyCounterpartOf: ReadonlyMap<number, number>;
  /** Per-concrete-struct-typeIndex, the set of sibling layout
   *  type-indices that could carry the same logical value at runtime.
   *  Used to dispatch the boundary reshape against multiple possible
   *  runtime shapes (e.g. anonymous tuples where `[usize, T]` →
   *  `[usize, Any]` is a different layout than `[Any, Any]`, and both
   *  may flow into a `[usize, string]` call site depending on which
   *  erased fn produced the value). Subset of `anyCounterpartOf` —
   *  the latter picks one canonical Any-version ; this one enumerates
   *  every sibling so the boundary reshape can switch on the runtime
   *  tag. */
  readonly siblingLayoutsOf: ReadonlyMap<number, readonly number[]>;
  /** Set when `--release` was passed. Gates `#line` directives in the per-op
   *  body emit — debug builds emit them so `gdb`/`lldb` step through the
   *  original Vader source. */
  readonly release: boolean;
}

function newCtx(m: BytecodeModule, release: boolean): EmitCtx {
  const fnNames = m.functions.map((f) => sanitise(f.name));
  const structNames: (string | null)[] = m.types.map((t) =>
    t.kind === "struct" ? `vader_struct_${sanitise(t.name)}_t` : null);
  let stringIdx = -1, errorIdx = -1;
  const primitiveTagOf = new Map<ValType, number>();
  for (let i = 0; i < m.types.length; i++) {
    const t = m.types[i]!;
    if (t.kind === "primitive") {
      primitiveTagOf.set(t.val, i);
      if (t.val === "string") stringIdx = i;
    }
    if (t.kind === "ref" && t.traitName === "Error") errorIdx = i;
  }
  const structIdxsByTrait = new Map<string, number[]>();
  for (const [structIdx, traits] of m.implTable) {
    for (const traitName of traits) {
      const list = structIdxsByTrait.get(traitName);
      if (list !== undefined) list.push(structIdx);
      else structIdxsByTrait.set(traitName, [structIdx]);
    }
  }
  const erasureGroups = computeErasureGroups(m);
  return {
    module: m, stringTagIndex: stringIdx, errorTagIndex: errorIdx,
    fnNames, structNames, primitiveTagOf, structIdxsByTrait,
    mayAlloc: computeMayAlloc(m),
    mutatedStructs: computeMutatedStructs(m),
    anyCounterpartOf: erasureGroups.anyCounterpartOf,
    siblingLayoutsOf: erasureGroups.siblingLayoutsOf,
    release,
  };
}

/** Group struct types by their source decl's symbol id ; for each group,
 *  identify the all-`Any`-args entry (every field's BcType is `ref` —
 *  i.e. an opaque heap pointer — which is the post-erasure shape) and
 *  link every other entry in the group to it. Used by `emitErasureBoundaryConversion`
 *  to recognise when a runtime box carries the heap-form layout of an
 *  otherwise inline-eligible struct.
 *
 *  Two grouping keys :
 *    - `symbolId` — set by `internStructDecl` for user `struct[T]` decls
 *      (Yield(T), Box(T), Repeat(T), …).
 *    - anonymous tuples — no symbolId ; group by `tuple:${arity}` so
 *      `[i32, string]` and `[Any, Any]` link as siblings. */
function groupStructsByShape(m: BytecodeModule): Map<string | number, number[]> {
  const groups = new Map<string | number, number[]>();
  for (let i = 0; i < m.types.length; i++) {
    const t = m.types[i]!;
    if (t.kind !== "struct") continue;
    const key = t.symbolId ?? (t.name.startsWith("__Tuple_") ? `tuple:${t.fields.length}` : undefined);
    if (key === undefined) continue;
    const list = groups.get(key);
    if (list !== undefined) list.push(i);
    else groups.set(key, [i]);
  }
  return groups;
}

function fieldsConvertible(m: BytecodeModule, iA: number, iB: number): boolean {
  const a = m.types[iA] as BcStruct;
  const b = m.types[iB] as BcStruct;
  if (a.fields.length !== b.fields.length) return false;
  for (let k = 0; k < a.fields.length; k++) {
    const va = valTypeOfBcType(m.types[a.fields[k]!.typeIndex]!);
    const vb = valTypeOfBcType(m.types[b.fields[k]!.typeIndex]!);
    if (va === vb) continue;
    if (va === "ref" || va === "any" || vb === "ref" || vb === "any") continue;
    return false;
  }
  return true;
}

function computeErasureGroups(m: BytecodeModule): {
  anyCounterpartOf: Map<number, number>;
  siblingLayoutsOf: Map<number, readonly number[]>;
} {
  const groups = groupStructsByShape(m);
  const anyCounterpartOf = new Map<number, number>();
  const siblingLayoutsOf = new Map<number, readonly number[]>();
  for (const indices of groups.values()) {
    if (indices.length < 2) continue;
    // Siblings : every other entry in the group whose fields are
    // pairwise convertible to ours. Anonymous-tuple grouping keys on
    // arity alone, so `[char, usize]` and `[string, ref]` land in the
    // same group ; reshape only makes sense when each field position
    // is either same-ValType or one side is ref/any (the
    // boxing/unboxing branches in `emitHeapStructReshapeForSibling`
    // cover those — anything else would emit an invalid C cast like
    // `(vader_string_t) some_uint32`).
    for (const i of indices) {
      const peers = indices.filter((j) => j !== i && fieldsConvertible(m, i, j));
      siblingLayoutsOf.set(i, peers);
    }
    // Any-counterpart : the entry whose fields are all heap refs (the
    // post-erasure shared shape). Concrete entries have at least one
    // primitive / typed field ; skip the group when no canonical
    // all-ref entry exists.
    const anyIdx = indices.find((i) => {
      const t = m.types[i] as { kind: "struct"; fields: readonly { typeIndex: number }[] };
      return t.fields.every((f) => m.types[f.typeIndex]?.kind === "ref");
    });
    if (anyIdx === undefined) continue;
    for (const i of indices) {
      if (i !== anyIdx) anyCounterpartOf.set(i, anyIdx);
    }
  }
  return { anyCounterpartOf, siblingLayoutsOf };
}

/** Walk every fn body once and collect the type indices of every struct
 *  that's a target of a `struct.set` / `struct.set_stack` op. Used by the
 *  inline-variant fast path : structs in this set must keep the heap rep
 *  so mutations are visible to aliases. */
function computeMutatedStructs(m: BytecodeModule): ReadonlySet<number> {
  const out = new Set<number>();
  for (const fn of m.functions) {
    for (const op of fn.body) {
      if (op.kind === "struct.set" || op.kind === "struct.set_stack") {
        out.add(op.typeIndex);
      }
    }
  }
  return out;
}

// Bytecode-level callgraph analysis: which fns may trigger a GC allocation
// (transitively). Used by `emitFunctionBody` to skip the shadow-stack frame
// for pure leaf-ish fns — `inc(x) = x + 1`, accessor methods, predicates.
//
// Direct allocators: `struct.new`, `array.new`, `array.push`, `string.concat`,
// `intrinsic` (string-builder family), `make_closure`. Indirect allocators
// (conservative — unknown target): `call.import`, `call.indirect`,
// `virtual.call`. Transitive: any `call` to a fn already in the set.
function computeMayAlloc(m: BytecodeModule): Set<number> {
  const mayAlloc = new Set<number>();

  for (let fi = 0; fi < m.functions.length; fi++) {
    const fn = m.functions[fi]!;
    for (const op of fn.body) {
      switch (op.kind) {
        case "struct.new":
        case "array.new":
        case "array.push":
        case "string.concat":
        case "intrinsic":
        case "make_closure":
        case "call.import":
        case "call.indirect":
        case "virtual.call":
          mayAlloc.add(fi);
          break;
      }
      if (mayAlloc.has(fi)) break;
    }
  }

  let changed = true;
  while (changed) {
    changed = false;
    for (let fi = 0; fi < m.functions.length; fi++) {
      if (mayAlloc.has(fi)) continue;
      const fn = m.functions[fi]!;
      for (const op of fn.body) {
        if (op.kind === "call" && mayAlloc.has(op.fnIndex)) {
          mayAlloc.add(fi);
          changed = true;
          break;
        }
      }
    }
  }

  return mayAlloc;
}

