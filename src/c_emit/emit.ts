function emitTypeDecls(ctx: EmitCtx, out: string[]): void {
  // Forward declarations for every struct so field references compile in any
  // order; bodies follow.
  for (let i = 0; i < ctx.module.types.length; i++) {
    const t = ctx.module.types[i]!;
    if (t.kind === "struct") {
      out.push(`typedef struct ${ctx.structNames[i]!} ${ctx.structNames[i]!};`);
    }
  }
  out.push(``);

  // Emit each struct's body plus, when applicable, a header-less mirror
  // twin used by the packed-payload fast path. Mirroring lets the C
  // compiler do the layout for fields packed directly into
  // `vader_box_t.payload.packed[]`. Mutated structs are excluded from
  // the inline-variant fast path so the mirror is never needed.
  for (let i = 0; i < ctx.module.types.length; i++) {
    const t = ctx.module.types[i]!;
    if (t.kind !== "struct") continue;
    const cname = ctx.structNames[i]!;
    out.push(`struct ${cname} {`);
    out.push(`    vader_obj_header_t header;`);
    for (const field of t.fields) {
      const fieldType = ctx.module.types[field.typeIndex]!;
      // `T | null` where T is a single heap type stores as a raw `void*` (NULL
      // for the null variant, an obj pointer for T). Saves 16 B vs the full
      // `vader_box_t` per field. The C emit at FieldGet / FieldSet boxes back
      // to / unboxes from `vader_box_t` so the rest of the program sees a
      // uniform value representation.
      if (fieldType.kind === "union" && nullableRefVariant(fieldType, ctx.module.types) !== null) {
        out.push(`    void* f_${sanitise(field.name)};`);
        continue;
      }
      out.push(`    ${cTypeFor(ctx, fieldType)} f_${sanitise(field.name)};`);
    }
    out.push(`};`);

    const isPacked = !ctx.mutatedStructs.has(i)
      && inlineVariantPayload(t, ctx.module.types) === "packed";
    if (isPacked) {
      out.push(`typedef struct {`);
      for (const field of t.fields) {
        const fieldType = ctx.module.types[field.typeIndex]!;
        out.push(`    ${cTypeFor(ctx, fieldType)} f_${sanitise(field.name)};`);
      }
      out.push(`} ${packedStructName(cname)};`);
    }
  }
  out.push(``);

  // The comptime atom table defines the `ATOM_LIT_<i>` u32 macros that
  // the per-literal `vader_str_<i>` aliases below resolve through, so
  // emit it first.
  emitAtomComptimeTable(ctx, out);

  // String literal aliases — each `vader_str_<i>` is now a `u32` (atom
  // id) initialised from the matching `ATOM_LIT_<i>` macro. body.ts and
  // static_table.ts still reference `vader_str_<i>` ; keeping the alias
  // means no rewriter pass is needed across the per-op emit logic.
  for (let i = 0; i < ctx.module.strings.length; i++) {
    out.push(`static const vader_string_t vader_str_${i} = ATOM_LIT_${i};`);
  }
  out.push(``);

  emitDataPool(ctx, out);
}

// =========================================================================
// Compile-time atom table — see docs/ATOM_INTERNING.md.
//
// Every string literal in the bytecode pool is serialised as a PERM atom
// in the binary's `.rodata`. The emitted `main` hands this table to
// `vader_atom_init_with_comptime` at startup ; the runtime memcpys it
// into the live atom table at indices `1 .. N` (index 0 is reserved for
// VADER_ATOM_EMPTY) and rebuilds the intern hash over it.
//
// The `ATOM_LIT_<i>` macros expose each literal's runtime atom id as a
// C compile-time constant — usable in `case ATOM_LIT_X:` once Phase 1
// flips `vader_string_t = u32` and the codegen emits literals as bare
// atom ids instead of `vader_str_<i>` fat-ptrs.
// =========================================================================

function emitAtomComptimeTable(ctx: EmitCtx, out: string[]): void {
  out.push(`/* Compile-time atom table — see docs/ATOM_INTERNING.md. */`);
  const strings = ctx.module.strings;
  if (strings.length === 0) {
    // Empty modules still emit a 1-entry placeholder so callers can take
    // `&vader_atom_comptime_table[0]` without UB ; the count of 0 tells
    // the runtime to ignore it.
    out.push(`static const vader_atom_entry_t vader_atom_comptime_table[1] = { { 0u, 0u, 0u, 0u, 0u, "" } };`);
    out.push(`#define VADER_COMPTIME_ATOM_COUNT 0u`);
    out.push(``);
    return;
  }

  // Blob : every literal's bytes followed by an inline NUL ("\0"
  // sentinel after each literal, so `data[len]` reads NUL for owners).
  // C concatenates the adjacent string literals into one `.rodata` array.
  const enc = new TextEncoder();
  const offsets: number[] = [];
  const lens: number[] = [];
  let offset = 0;
  const blobLines: string[] = [];
  for (let i = 0; i < strings.length; i++) {
    const bytes = enc.encode(strings[i]!);
    blobLines.push(`    ${cStringLitFromBytes(bytes)} "\\0"`);
    offsets.push(offset);
    lens.push(bytes.length);
    offset += bytes.length + 1;
  }
  out.push(`static const char vader_atom_blob[] =`);
  for (const line of blobLines) out.push(line);
  out.push(`;`);
  out.push(``);

  // Entry table — one PERM owner per literal, pointing into the blob.
  // Field order : { parent, parent_offset, len, flags, _pad, data }.
  out.push(`static const vader_atom_entry_t vader_atom_comptime_table[] = {`);
  for (let i = 0; i < strings.length; i++) {
    out.push(`    { 0u, 0u, ${lens[i]}u, VADER_ATOM_FLAG_PERM, 0u, &vader_atom_blob[${offsets[i]}] },`);
  }
  out.push(`};`);
  out.push(``);

  // Macros : `ATOM_LIT_<i>` is the runtime atom id for the i-th literal
  // in `ctx.module.strings`. +1 because the runtime installs the empty
  // atom at index 0 before the comptime block.
  for (let i = 0; i < strings.length; i++) {
    out.push(`#define ATOM_LIT_${i} ${i + 1}u`);
  }
  out.push(`#define VADER_COMPTIME_ATOM_COUNT ${strings.length}u`);
  out.push(``);
}

// =========================================================================
// Data pool — module-level `const T[]` literals emitted as `.rodata` statics.
// Each pool entry produces :
//   - a packed wrapper struct `__vader_data_<i>_t` holding the buf + slots
//   - a static const vader_array_t header pointing into the wrapper
// `data.const <i>` op resolves to `vader_box_obj(<type>, &__vader_data_<i>)`.
// The GC scanner skips static pointers via its arena bound checks.
// =========================================================================

function emitDataPool(ctx: EmitCtx, out: string[]): void {
  if (ctx.module.dataPool.length === 0) return;
  for (let i = 0; i < ctx.module.dataPool.length; i++) {
    emitDataEntry(i, ctx.module.dataPool[i]!, out);
  }
  out.push(``);
}

function emitDataEntry(idx: number, entry: BcDataEntry, out: string[]): void {
  const cType = cTypeForArrayKind(entry.kind);
  const kindIdx = arrayKindIndex(entry.kind);
  const length = entry.items.length;
  // Wrapper packs the buf header + a fixed-size slot array. The buf's
  // `slots` flex-array would otherwise prevent static initialisation ;
  // the wrapper sidesteps that by giving the slots their own storage.
  // First member is `vader_array_buf_t` so `(vader_array_buf_t*) &w.buf`
  // is a legal cast.
  out.push(`static const struct {`);
  out.push(`    vader_array_buf_t buf;`);
  out.push(`    ${cType} slots[${length}];`);
  out.push(`} __vader_data_buf_${idx} = {`);
  out.push(`    .buf = {`);
  out.push(`        .header = { VADER_TYPE_INDEX_ARRAY_BUF, 0, 0, 0, NULL },`);
  out.push(`        .capacity = ${length}u, .length = ${length}u,`);
  out.push(`        .element_kind = ${kindIdx}u, ._pad = {0,0,0,0,0,0,0}`);
  out.push(`    },`);
  out.push(`    .slots = { ${entry.items.map((v) => formatDataItem(entry.kind, v)).join(", ")} }`);
  out.push(`};`);
  out.push(`static const vader_array_t __vader_data_${idx} = {`);
  out.push(`    .header = { 0, 0, 0, 0, NULL },`);
  out.push(`    .length = ${length}u, .capacity = ${length}u, .offset = 0u,`);
  out.push(`    .buf = (vader_array_buf_t*) &__vader_data_buf_${idx}.buf,`);
  out.push(`};`);
}

function cTypeForArrayKind(k: ArrayKind): string {
  switch (k) {
    case "u8":   return "uint8_t";
    case "u16":  return "uint16_t";
    case "u32":  return "uint32_t";
    case "u64":  return "uint64_t";
    case "i8":   return "int8_t";
    case "i16":  return "int16_t";
    case "i32":  return "int32_t";
    case "i64":  return "int64_t";
    case "f32":  return "float";
    case "f64":  return "double";
    case "char": return "uint32_t";
    case "bool": return "uint8_t";
    case "boxed":
      throw new Error("c-emit: data pool kind 'boxed' not allowed");
  }
}

function formatDataItem(kind: ArrayKind, v: bigint): string {
  switch (kind) {
    case "u8":   return `${BigInt.asUintN(8, v).toString()}u`;
    case "u16":  return `${BigInt.asUintN(16, v).toString()}u`;
    case "u32":  return `${BigInt.asUintN(32, v).toString()}u`;
    case "u64":  return `UINT64_C(${BigInt.asUintN(64, v).toString()})`;
    case "i8":   return `${BigInt.asIntN(8, v).toString()}`;
    case "i16":  return `${BigInt.asIntN(16, v).toString()}`;
    case "i32":  return i32LitC(Number(BigInt.asIntN(32, v)));
    case "i64":  return i64LitC(BigInt.asIntN(64, v));
    case "char": return `${BigInt.asUintN(32, v).toString()}u`;
    case "bool": return v === 0n ? "0u" : "1u";
    case "f32":  return floatLit(f32FromBits(Number(BigInt.asUintN(32, v))), "f");
    case "f64":  return floatLit(f64FromBits(BigInt.asUintN(64, v)), "");
    case "boxed":
      throw new Error("c-emit: data pool kind 'boxed' not allowed");
  }
}

const FLOAT_BUF = new ArrayBuffer(8);
const FLOAT_DV = new DataView(FLOAT_BUF);

function f32FromBits(bits: number): number {
  FLOAT_DV.setUint32(0, bits, true);
  return FLOAT_DV.getFloat32(0, true);
}

function f64FromBits(bits: bigint): number {
  FLOAT_DV.setBigUint64(0, bits, true);
  return FLOAT_DV.getFloat64(0, true);
}

