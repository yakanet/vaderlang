/** C keywords (C11 + a few common identifiers like `restrict` reserved by
 *  many compilers). Any user identifier that happens to match one is suffixed
 *  with `_v` so the emitted C is always valid. */
const C_RESERVED: ReadonlySet<string> = new Set([
  "auto", "break", "case", "char", "const", "continue", "default", "do",
  "double", "else", "enum", "extern", "float", "for", "goto", "if", "inline",
  "int", "long", "register", "restrict", "return", "short", "signed",
  "sizeof", "static", "struct", "switch", "typedef", "union", "unsigned",
  "void", "volatile", "while", "_Alignas", "_Alignof", "_Atomic", "_Bool",
  "_Complex", "_Generic", "_Imaginary", "_Noreturn", "_Static_assert",
  "_Thread_local",
]);

export function sanitise(raw: string): string {
  const out = raw.replace(/[^A-Za-z0-9_]/g, "_");
  return C_RESERVED.has(out) ? `${out}_v` : out;
}

export function cStringLit(s: string): string {
  return cStringLitFromBytes(new TextEncoder().encode(s));
}

export function cStringLitFromBytes(bytes: Uint8Array): string {
  let out = '"';
  for (let i = 0; i < bytes.length; i++) {
    const b = bytes[i]!;
    if (b === 0x5c) out += "\\\\";
    else if (b === 0x22) out += "\\\"";
    else if (b === 0x0a) out += "\\n";
    else if (b === 0x0d) out += "\\r";
    else if (b === 0x09) out += "\\t";
    else if (b < 0x20 || b >= 0x7f) {
      // C `\x` escapes are unbounded — they consume every following hex digit
      // until the first non-hex character. If the next byte happens to be a
      // hex digit (or a continuation byte from a multi-byte UTF-8 codepoint),
      // close the escape with `""` so the digits start a fresh literal piece.
      out += `\\x${b.toString(16).padStart(2, "0")}`;
      const next = bytes[i + 1];
      if (next !== undefined && isCHexDigit(next)) out += '""';
    } else out += String.fromCharCode(b);
  }
  out += '"';
  return out;
}

function isCHexDigit(b: number): boolean {
  return (b >= 0x30 && b <= 0x39) || (b >= 0x41 && b <= 0x46) || (b >= 0x61 && b <= 0x66);
}

export function escapeC(s: string): string {
  return s.replace(/\\/g, "\\\\").replace(/"/g, "\\\"");
}

export function floatLit(v: number, suffix: string): string {
  if (!Number.isFinite(v)) {
    if (Number.isNaN(v)) return "(0.0/0.0)";
    return v > 0 ? "(1.0/0.0)" : "(-1.0/0.0)";
  }
  const s = v.toString();
  return s.includes(".") || s.includes("e") ? s + suffix : `${s}.0${suffix}`;
}

const I32_MIN = -2147483648;
const I32_MAX =  2147483647;
const I64_MIN_BIG = -(1n << 63n);
const I64_MAX_BIG =  (1n << 63n) - 1n;

/** C source for an i32 literal. Values outside the signed range round-trip
 *  through `(int32_t)UINT32_C(N)` to silence -Wconstant-conversion: the
 *  literal is born unsigned, then reinterpreted via two's complement.
 *  `INT32_MIN` itself can't be written as `INT32_C(-2147483648)` — that
 *  parses as unary minus applied to `2147483648`, which doesn't fit i32
 *  and is UB on strict toolchains; emit `INT32_MIN` (a stdint.h macro). */
export function i32LitC(value: number): string {
  if (value === I32_MIN) return `INT32_MIN`;
  if (value >= I32_MIN && value <= I32_MAX) return `INT32_C(${value})`;
  return `(int32_t)UINT32_C(${value >>> 0})`;
}

/** C source for an i64 literal. Same logic as `i32LitC` but for the 64-bit
 *  range. The bigint stays exact so unsigned values up to 2^64-1 round-trip
 *  through `(int64_t)UINT64_C(N)`. `INT64_MIN` likewise needs the macro to
 *  dodge the unary-minus / overflow trap. */
export function i64LitC(value: bigint): string {
  if (value === I64_MIN_BIG) return `INT64_MIN`;
  if (value >= I64_MIN_BIG && value <= I64_MAX_BIG) return `INT64_C(${value.toString()})`;
  const u = value & ((1n << 64n) - 1n);
  return `(int64_t)UINT64_C(${u.toString()})`;
}
