// Type info table — pointer maps consumed by the GC scanner
// =========================================================================

function emitTypeInfoTable(ctx: EmitCtx, out: string[]): void {
  const types = ctx.module.types;

  // Per-type ptr-offset arrays. We collect into a buffer first so the table
  // initialiser can reference each by name. `offsetof` is resolved at C compile
  // time so we don't have to know struct sizes in TypeScript.
  //
  // Two parallel arrays per struct:
  //   - `_ptrs[]`     box-bearing fields (vader_box_t)
  //   - `_strs[]`     raw vader_string_t fat-ptr fields
  // The string GC needs the latter because string fields are stored inline
  // as primitive `vader_string_t`, not as boxes — the box-aliased mark
  // walk would otherwise miss them.
  for (let i = 0; i < types.length; i++) {
    const t = types[i]!;
    if (t.kind === "struct") {
      const cname = ctx.structNames[i]!;
      const offsets: string[] = [];
      const stringOffsets: string[] = [];
      const refOffsets: string[] = [];
      for (const field of t.fields) {
        const ftype = types[field.typeIndex]!;
        if (ftype.kind === "union" && nullableRefVariant(ftype, types) !== null) {
          // `T | null` field — stored as a raw `void*` (NULL = null variant,
          // non-null = obj pointer). Saves 16 B vs the equivalent box ;
          // listed separately so the scanner walks it with `scan_raw`.
          refOffsets.push(`offsetof(${cname}, f_${sanitise(field.name)})`);
        } else if (ftype.kind !== "primitive") {
          // Heap-bearing field — its C type is `vader_box_t`. Tracing this
          // slot requires checking the box tag at scan time.
          offsets.push(`offsetof(${cname}, f_${sanitise(field.name)})`);
        } else if (ftype.val === "string") {
          // Raw `vader_string_t` field. The Cheney scan ignores it (the
          // buffer lives in the non-moving string arena), but the string
          // mark-sweep collector must reach it.
          stringOffsets.push(`offsetof(${cname}, f_${sanitise(field.name)})`);
        }
      }
      if (offsets.length > 0) {
        out.push(`static const uint16_t vader_type_${i}_ptrs[] = { ${offsets.join(", ")} };`);
      }
      if (stringOffsets.length > 0) {
        out.push(`static const uint16_t vader_type_${i}_strs[] = { ${stringOffsets.join(", ")} };`);
      }
      if (refOffsets.length > 0) {
        out.push(`static const uint16_t vader_type_${i}_refs[] = { ${refOffsets.join(", ")} };`);
      }
    } else if (t.kind === "fn") {
      // vader_fn_t has `code` (function pointer — NOT a heap ref, must not
      // be scanned) and `env` (heap pointer to the closure environment).
      out.push(`static const uint16_t vader_type_${i}_ptrs[] = { offsetof(vader_fn_t, env) };`);
    } else if (t.kind === "array") {
      // The buf is a raw pointer to a header-bearing object — scan_raw treatment.
      out.push(`static const uint16_t vader_type_${i}_ptrs[] = { offsetof(vader_array_t, buf) };`);
    }
  }

  out.push(``);
  // Fixed length so missing entries (primitives, unions, refs — no GC info)
  // zero-initialise to `VADER_TYPE_KIND_NONE`. Without the explicit size, C
  // would size the array to `max-designated-index + 1`, and any later
  // `vader_type_info_table[tag]` read with `tag` past the last designated
  // entry but below `vader_type_info_count` would walk off the array.
  out.push(`const vader_type_info_t vader_type_info_table[${types.length}] = {`);
  for (let i = 0; i < types.length; i++) {
    const t = types[i]!;
    if (t.kind === "struct") {
      // Inline-variant structs (empty-body / single-primitive-field /
      // single-ref-field / multi-field POD packed into `payload.packed[]`)
      // are encoded directly inside `vader_box_t.payload` — they never
      // get a heap allocation. Mutated structs are excluded : they keep
      // the heap rep so `struct.set` is observable across aliases.
      const inlinePayload = !ctx.mutatedStructs.has(i)
        ? inlineVariantPayload(t, types)
        : null;
      if (inlinePayload === "void" || (inlinePayload !== null && inlinePayload !== "ref")) {
        // Empty / single-primitive / packed-multi-field : `payload` is
        // non-pointer data, GC skips the slot. Leave the entry at its
        // zero-init KIND_NONE so `vader_gc_scan_box` short-circuits before
        // touching the payload.
        continue;
      }
      if (inlinePayload === "ref") {
        // Single-ref-field inline : `payload.obj` IS the referent's heap
        // pointer (the wrapper struct itself never allocates). GC reaches
        // through via `scan_raw`, reading the referent's own type tag from
        // its header — no per-tag scan info needed here.
        out.push(`    [${i}] = { VADER_TYPE_KIND_INLINE_REF, 0, NULL, 0, 0, NULL, 0, NULL },`);
        continue;
      }
      const cname = ctx.structNames[i]!;
      const boxFields = t.fields.filter((f) => {
        const ft = types[f.typeIndex]!;
        if (ft.kind === "union" && nullableRefVariant(ft, types) !== null) return false;
        return ft.kind !== "primitive";
      });
      const strFields = t.fields.filter((f) => {
        const ft = types[f.typeIndex]!;
        return ft.kind === "primitive" && ft.val === "string";
      });
      const refFields = t.fields.filter((f) => {
        const ft = types[f.typeIndex]!;
        return ft.kind === "union" && nullableRefVariant(ft, types) !== null;
      });
      const ptrArr = boxFields.length > 0 ? `vader_type_${i}_ptrs` : "NULL";
      const strArr = strFields.length > 0 ? `vader_type_${i}_strs` : "NULL";
      const refArr = refFields.length > 0 ? `vader_type_${i}_refs` : "NULL";
      out.push(`    [${i}] = { VADER_TYPE_KIND_STRUCT, sizeof(${cname}), ${ptrArr}, ${boxFields.length}, ${strFields.length}, ${strArr}, ${refFields.length}, ${refArr} },`);
    } else if (t.kind === "fn") {
      out.push(`    [${i}] = { VADER_TYPE_KIND_FN, sizeof(vader_fn_t), vader_type_${i}_ptrs, 1, 0, NULL, 0, NULL },`);
    } else if (t.kind === "array") {
      out.push(`    [${i}] = { VADER_TYPE_KIND_ARRAY, sizeof(vader_array_t), vader_type_${i}_ptrs, 1, 0, NULL, 0, NULL },`);
    }
    // primitive / union / ref → entry implicitly zero-initialised
    // (VADER_TYPE_KIND_NONE), which the GC interprets as "non-heap, skip".
  }
  out.push(`};`);
  out.push(`const size_t vader_type_info_count = ${types.length};`);
  out.push(``);

  // Per-type size table — backs the runtime path of `@size_of(t)`. Indexed
  // by typeIndex (the same slot `type.const N` encodes) ; the table is
  // emitted unconditionally so the placeholder `type.const` box payload
  // round-trip stays valid regardless of whether the user code actually
  // reads it. Sizes computed by the shared `sizeOfBcType` helper.
  out.push(`const size_t vader_type_size[${types.length}] = {`);
  for (let i = 0; i < types.length; i++) {
    out.push(`    [${i}] = ${sizeOfBcType(types[i]!)}u,`);
  }
  out.push(`};`);
  out.push(``);

  // Per-tag trait-method vtables. Override the weak fallback in
  // `vader_runtime.c` so `vader_virtual_dispatch` resolves correctly for
  // any concrete type implementing a trait.
  emitVtables(ctx.module, ctx.fnNames, out);
}

function emitFnTrampolines(ctx: EmitCtx, out: string[]): void {
  // Walk every fn body to discover which fns are taken by reference and
  // which lifted fns get instantiated via `make_closure`. Each kind needs a
  // matching wrapper:
  //   - `fn.ref`        — wrapper drops env (global fn used as a value)
  //   - `make_closure`  — wrapper boxes env into vader_box_t before calling
  //                       (the lifted fn's signature takes `ref env`)
  const fnRefMap = new Map<number, number>();        // fnIndex → BcFn typeIndex
  const closureMap = new Map<number, number>();      // lifted fnIndex → BcFn typeIndex
  for (const fn of ctx.module.functions) {
    for (const op of fn.body) {
      if (op.kind === "fn.ref") fnRefMap.set(op.fnIndex, op.typeIndex);
      else if (op.kind === "make_closure") closureMap.set(op.fnIndex, op.typeIndex);
    }
  }
  if (fnRefMap.size === 0 && closureMap.size === 0) return;

  // Trampolines + lift wrappers use a canonical erased signature
  // (vader_box_t for every arg + return) so a single function pointer
  // can be safely cast at any `call.indirect` site regardless of
  // whether the static fn type at the call site is concrete
  // (`(i32) -> i32`) or erased (`(Any) -> Any`). Inside, each arg gets
  // unboxed to the underlying fn's concrete ValType, the actual fn is
  // called, the return is boxed. Costs one box+unbox per arg+return
  // per indirect call — acceptable for the correctness gain ;
  // statically-known direct calls still hit `emitCall` and skip the
  // wrapper entirely.
  for (const [fnIndex, typeIndex] of fnRefMap) {
    const fn = ctx.module.functions[fnIndex]!;
    const cname = ctx.fnNames[fnIndex]!;
    const tailParams = fn.signature.params.map((_, j) => `vader_box_t a${j}`);
    const head = tailParams.length === 0 ? "void* env" : `void* env, ${tailParams.join(", ")}`;
    out.push(`static vader_box_t vader_fn_tramp_${fnIndex}(${head}) {`);
    out.push(`    (void) env;`);
    const unboxedArgs = fn.signature.params.map((p, j) => unboxExpr(`a${j}`, p)).join(", ");
    if (fn.signature.result === "void") {
      out.push(`    ${cname}(${unboxedArgs});`);
      out.push(`    return vader_box_null();`);
    } else {
      const callExpr = `${cname}(${unboxedArgs})`;
      const boxed = fn.signature.result === "ref" || fn.signature.result === "any"
        ? callExpr
        : boxExpr(ctx, callExpr, fn.signature.result, fn.signature.resultType);
      out.push(`    return ${boxed};`);
    }
    out.push(`}`);
    out.push(`static vader_fn_t vader_fn_static_${fnIndex} = { { ${typeIndex}u, 0u, 0u, 0u, NULL }, (void*) &vader_fn_tramp_${fnIndex}, NULL };`);
  }

  for (const [fnIndex, _typeIndex] of closureMap) {
    const fn = ctx.module.functions[fnIndex]!;
    const cname = ctx.fnNames[fnIndex]!;
    if (fn.signature.params.length === 0) continue;
    // Skip the env slot (index 0) — the wrapper takes `void* env`
    // and boxes it back into the vader_box_t the lifted fn expects.
    const tailVals = fn.signature.params.slice(1);
    const tailParams = tailVals.map((_, j) => `vader_box_t a${j}`);
    const head = tailParams.length === 0 ? "void* env" : `void* env, ${tailParams.join(", ")}`;
    out.push(`static vader_box_t vader_fn_lift_${fnIndex}(${head}) {`);
    out.push(`    vader_box_t env_box; env_box.tag = 0u; env_box._pad = 0u; env_box.payload.obj = env;`);
    const unboxedTail = tailVals.map((p, j) => unboxExpr(`a${j}`, p)).join(", ");
    const callArgs = unboxedTail.length === 0 ? `env_box` : `env_box, ${unboxedTail}`;
    if (fn.signature.result === "void") {
      out.push(`    ${cname}(${callArgs});`);
      out.push(`    return vader_box_null();`);
    } else {
      const callExpr = `${cname}(${callArgs})`;
      const boxed = fn.signature.result === "ref" || fn.signature.result === "any"
        ? callExpr
        : boxExpr(ctx, callExpr, fn.signature.result, fn.signature.resultType);
      out.push(`    return ${boxed};`);
    }
    out.push(`}`);
  }
  out.push(``);
}

